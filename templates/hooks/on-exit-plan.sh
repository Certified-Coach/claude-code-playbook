#!/bin/bash
# Hook: PostToolUse → ExitPlanMode
# Commits plan markdown to docs/plans/, posts issue comment with link.
#
# Behaviour:
#   - If a plan file exists: commit markdown, post issue comment with link
#   - If no plan file: post issue comment noting "exited without a plan"
#   - On re-entry: version plans (v2, v3, etc.) with addendum headers
#
# Requires: gh CLI, jq
# Optional: pandoc + chrome (for PDF — not included in v1 template)
set -uo pipefail

INPUT=$(cat)
SESSION_ID=$(echo "$INPUT" | jq -r '.session_id // "unknown"')
CWD=$(echo "$INPUT" | jq -r '.cwd // "."')

cd "$CWD"

MARKER_DIR="/tmp/claude-plan-sessions"
PLANS_DIR="docs/plans"
mkdir -p "$PLANS_DIR"

# ── Detect issue number from branch name ──────────────────
BRANCH=$(git branch --show-current 2>/dev/null || echo "")
ISSUE_NUMBER=$(echo "$BRANCH" | grep -oE '^[0-9]+' || echo "")

# ── Find the plan file ────────────────────────────────────
PLAN_FILE=""
CLAUDE_PLANS_DIR="$HOME/.claude/plans"
if [ -d "$CLAUDE_PLANS_DIR" ]; then
  # Primary: files newer than planning marker
  if [ -f "${MARKER_DIR}/${SESSION_ID}.planning" ]; then
    PLAN_FILE=$(/usr/bin/find "$CLAUDE_PLANS_DIR" -name "*.md" -type f -newer "${MARKER_DIR}/${SESSION_ID}.planning" 2>/dev/null | head -1)
  fi
  # Fallback: most recent plan file modified in last 30 minutes
  if [ -z "$PLAN_FILE" ]; then
    PLAN_FILE=$(/usr/bin/find "$CLAUDE_PLANS_DIR" -name "*.md" -type f -mmin -30 2>/dev/null | xargs ls -t 2>/dev/null | head -1)
  fi
fi

if [ -z "$ISSUE_NUMBER" ]; then
  echo "Could not determine issue number from branch: ${BRANCH}" >&2
  exit 0
fi

# ── No plan file → post "exited without plan" comment ─────
if [ -z "$PLAN_FILE" ] || [ ! -f "$PLAN_FILE" ]; then
  echo "No plan file found — posting 'exited without plan' comment to #${ISSUE_NUMBER}" >&2
  gh issue comment "$ISSUE_NUMBER" --body "### Planning session exited without a plan

A planning session was initiated but no plan document was produced. The implementation will proceed based on the issue's existing scope and acceptance criteria.

_Recorded automatically by plan lifecycle hook — $(date -u +%Y-%m-%dT%H:%M:%SZ)_" 2>/dev/null || true
  exit 0
fi

# ── Determine version number ─────────────────────────────
SLUG=$(echo "$BRANCH" | sed "s/^${ISSUE_NUMBER}-//")
BASE_NAME="${ISSUE_NUMBER}-${SLUG}"
EXISTING_COUNT=$(ls "${PLANS_DIR}/${BASE_NAME}"*.md 2>/dev/null | wc -l | tr -d ' ')

if [ "$EXISTING_COUNT" -eq 0 ]; then
  MD_NAME="${BASE_NAME}.md"
  VERSION_LABEL=""
else
  VERSION=$((EXISTING_COUNT + 1))
  MD_NAME="${BASE_NAME}-v${VERSION}.md"
  VERSION_LABEL=" (v${VERSION})"
fi

# ── Copy plan markdown to docs/plans/ ─────────────────────
cp "$PLAN_FILE" "${PLANS_DIR}/${MD_NAME}"

# ── Add addendum header for v2+ plans ─────────────────────
if [ -n "$VERSION_LABEL" ]; then
  PREV_VERSION=$((VERSION - 1))
  if [ "$PREV_VERSION" -eq 1 ]; then
    PREV_MD="${BASE_NAME}.md"
  else
    PREV_MD="${BASE_NAME}-v${PREV_VERSION}.md"
  fi
  ADDENDUM_HEADER="---
> **Plan addendum${VERSION_LABEL}** for issue #${ISSUE_NUMBER}
> Previous version: [${PREV_MD}](${PREV_MD})
> Generated: $(date -u +%Y-%m-%dT%H:%M:%SZ)
---

"
  printf '%s' "$ADDENDUM_HEADER" | cat - "${PLANS_DIR}/${MD_NAME}" > "/tmp/plan-addendum-$$.md"
  mv "/tmp/plan-addendum-$$.md" "${PLANS_DIR}/${MD_NAME}"
fi

# ── Commit plan file ──────────────────────────────────────
git add "${PLANS_DIR}/${MD_NAME}" 2>/dev/null

if git diff --cached --quiet 2>/dev/null; then
  echo "No changes to commit — plan file already exists" >&2
else
  git commit -m "DOCS: Implementation plan${VERSION_LABEL} for #${ISSUE_NUMBER}

Auto-generated by plan lifecycle hook.

Co-Authored-By: Claude Opus 4.6 (1M context) <noreply@anthropic.com>" 2>/dev/null || true
fi

# ── Post issue comment ────────────────────────────────────
REPO_URL=$(gh repo view --json url --jq '.url' 2>/dev/null || echo "")
PLAN_LINK="${REPO_URL}/blob/${BRANCH}/${PLANS_DIR}/${MD_NAME}"

gh issue comment "$ISSUE_NUMBER" --body "### Implementation plan approved${VERSION_LABEL}

**Plan:** [${MD_NAME}](${PLAN_LINK})

_Recorded automatically by plan lifecycle hook — $(date -u +%Y-%m-%dT%H:%M:%SZ)_" 2>/dev/null || true

echo "Plan${VERSION_LABEL} generated and posted to #${ISSUE_NUMBER}" >&2

# ── Mark planning as complete ─────────────────────────────
if [ -f "${MARKER_DIR}/${SESSION_ID}.planning" ]; then
  mv "${MARKER_DIR}/${SESSION_ID}.planning" "${MARKER_DIR}/${SESSION_ID}.planned"
fi

exit 0
